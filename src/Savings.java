public class Savings {
    private String customerNumber;
    private String customerName;
    private String initialDeposit;
    private String numOfYears;
    private String typeOfSavings;

    public Savings(String customerNumber, String customerName, String initialDeposit, String numOfYears, String typeOfSavings) {
        this.customerNumber = customerNumber;
        this.customerName = customerName;
        this.initialDeposit = initialDeposit;
        this.numOfYears = numOfYears;
        this.typeOfSavings = typeOfSavings;
    }


}
